package com.app.urbfs.model;

public class LevelDetailsModel {
    private String newid;
    private String newidname;
    private String sponcerid;
    private String sponcername;
    private String date;
    private String city;
    private String amount;
    private String pinval;

    public String getNewid() {
        return newid;
    }

    public void setNewid(String newid) {
        this.newid = newid;
    }

    public String getNewidname() {
        return newidname;
    }

    public void setNewidname(String newidname) {
        this.newidname = newidname;
    }

    public String getSponcerid() {
        return sponcerid;
    }

    public void setSponcerid(String sponcerid) {
        this.sponcerid = sponcerid;
    }

    public String getSponcername() {
        return sponcername;
    }

    public void setSponcername(String sponcername) {
        this.sponcername = sponcername;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getPinval() {
        return pinval;
    }

    public void setPinval(String pinval) {
        this.pinval = pinval;
    }
}
